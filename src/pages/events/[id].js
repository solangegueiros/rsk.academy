import PropTypes from 'prop-types'
import {
  Box,
  Heading,
  HStack,
  Icon,
  Image,
  Text,
  Link,
  Button,
  VStack,
  Spinner,
  Flex,
  SimpleGrid,
  useColorModeValue,
  AspectRatio,
} from '@chakra-ui/react'
import dynamic from 'next/dynamic'
import React, { useEffect, useState } from 'react'
import { FaTwitter } from 'react-icons/fa'
import { IoLanguage } from 'react-icons/io5'
import { RiVideoChatFill, RiMapPin2Line } from 'react-icons/ri'
import { GoRepo } from 'react-icons/go'
import { useI18n } from 'next-localization'
import { isPast } from 'date-fns'
import { useRouter } from 'next/router'

import { Layout } from '@/components/all'
import Seo from '@/components/Seo'
import { getEvents } from 'lib/api'

const TimeZone = dynamic(() => import('../../components/TimeZone'), {
  ssr: false,
})

const LANGUAGES = {
  en: 'English',
  es: 'Español',
  pt: 'Português',
}

const Event = ({ event }) => {
  const { t } = useI18n()
  const bg = useColorModeValue('white', 'dark.400')
  const { query } = useRouter()
  const [eventItem, setEventItem] = useState(event)

  useEffect(() => {
    const fetchEvent = async () => {
      const response = await fetch(`/api/event/${query.id}`)
      const result = await response.json()
      setEventItem(result)
    }
    fetchEvent()
  }, [])

  if (!event)
    return (
      <Flex h='100vh' align='center' justify='center'>
        <Spinner size='lg'>No Event</Spinner>
      </Flex>
    )

  const {
    datetime,
    title,
    language,
    speaker,
    role,
    twitter,
    city,
    description,
    webinar_link,
    rsvp_embed,
    image,
    video_link,
    resources,
  } = eventItem

  const isExpired = isPast(new Date(datetime))
  const videoId = video_link?.split('/').slice(-1)[0]

  return (
    <Layout>
      <Seo title={title} />
      <Heading my={8} as='h1'>
        {title}
      </Heading>
      <Text fontSize='1.2em'>{description}</Text>
      <Box my={8} mx='auto' bg={bg}>
        <Image w='full' src={image} />
      </Box>

      <SimpleGrid
        p={8}
        bg={bg}
        alignItems='center'
        columns={{ base: 1, md: 2 }}
        gap={8}
      >
        <VStack spacing={4} align='flex-start' fontSize='1.2em'>
          <TimeZone timeStr={datetime} spacing={4} />
          <HStack>
            <Icon as={IoLanguage} />
            <Text>{LANGUAGES[language]}</Text>
          </HStack>
          <HStack>
            <Icon as={RiMapPin2Line} />
            <Text>{city}</Text>
          </HStack>
          <HStack
            as={Link}
            rel='noopener noreferrer'
            target='_blank'
            href={`https://twitter.com/${twitter}`}
          >
            <Icon as={FaTwitter} />
            <Text>
              {speaker} | {role}
            </Text>
          </HStack>
        </VStack>
        <VStack align={{ base: 'center', md: 'flex-end' }} spacing={4}>
          {!isExpired && (
            <Button
              size='lg'
              target='_blank'
              rel='noopener noreferrer'
              variant='normal'
              as='a'
              leftIcon={<RiVideoChatFill />}
              href={webinar_link}
              isDisabled={!webinar_link}
            >
              Go to webinar
            </Button>
          )}

          <Button
            size='lg'
            target='_blank'
            rel='noopener noreferrer'
            variant='normal'
            as='a'
            leftIcon={<GoRepo />}
            href={resources}
            isDisabled={!resources}
          >
            Resources
          </Button>
        </VStack>
      </SimpleGrid>
      {!video_link && (
        <Box p={8} mt={8} boxShadow='md' bg={bg}>
          <Heading textAlign='center' as='h3' size='md' mb={8}>
            {t('register')}
          </Heading>
          <Box w='full' minH='450px' as='iframe' src={rsvp_embed} />
        </Box>
      )}
      {video_link && (
        <AspectRatio ratio={16 / 9}>
          <Box
            title={title}
            allowFullScreen={true}
            as='iframe'
            allow='accelerometer; autoplay; clipboard-write; encrypted-media;'
            src={`https://youtube.com/embed/${videoId}`}
          />
        </AspectRatio>
      )}
    </Layout>
  )
}

export async function getStaticPaths() {
  const response = await getEvents()
  const paths = response.map(e => ({ params: { id: e.id } }))

  return {
    paths,
    fallback: true,
  }
}

export async function getStaticProps({ params: { id } }) {
  const response = await getEvents()
  const event = response.find(e => e.id === id)
  return {
    props: {
      event,
    },
  }
}

Event.propTypes = {
  event: PropTypes.shape({
    datetime: PropTypes.string.isRequired,
    title: PropTypes.string.isRequired,
    language: PropTypes.string.isRequired,
    speaker: PropTypes.string.isRequired,
    role: PropTypes.string.isRequired,
    twitter: PropTypes.string.isRequired,
    city: PropTypes.string.isRequired,
    description: PropTypes.string,
    webinar_link: PropTypes.string,
    rsvp_embed: PropTypes.string,
    image: PropTypes.string,
    video_link: PropTypes.string,
    resources: PropTypes.string,
  }),
}

export default Event
